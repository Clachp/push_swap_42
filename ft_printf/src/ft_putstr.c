#include "../ft_printf.h"

size_t	ft_putstr(char *s)
{
	char	*t;

	if (s == NULL)
		s = "(null)";
	t = s;
	while (s && *s)
		write (1, &*s++, 1);
	return (ft_strlen(t));
}
