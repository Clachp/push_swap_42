#include "../ft_printf.h"

size_t	ft_putptr(unsigned long long int n)
{
	if (n == 0)
	{
		write (1, "(nil)", 5);
		return (5);
	}
	write (1, "0x", 2);
	return (ft_puthexa(n, 'x') + 2);
}
